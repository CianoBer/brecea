CREATE PROCEDURE.CHIAVEPRIMARIA @STRNOMETABELLA VARCHAR(128), @STRCAMPICHIAVE VARCHAR(200) OUTPUT AS
BEGIN
  DECLARE @STRRES VARCHAR(200)
  DECLARE @STRNOME VARCHAR(128)
  SET @STRRES=''
  DECLARE CURCOLONNE CURSOR FOR
		SELECT SYSCOLUMNS.NAME
		FROM (SYSCOLUMNS INNER JOIN
			SYSOBJECTS ON SYSCOLUMNS.ID = SYSOBJECTS.ID) INNER JOIN
			SYSINDEXKEYS ON SYSINDEXKEYS.ID = SYSOBJECTS.ID
		WHERE SYSOBJECTS.NAME = @STRNOMETABELLA AND
			SYSINDEXKEYS.INDID = 1 AND
			SYSINDEXKEYS.COLID = SYSCOLUMNS.COLID
  OPEN CURCOLONNE
  FETCH NEXT FROM CURCOLONNE INTO @STRNOME
  WHILE @@FETCH_STATUS = 0
    BEGIN
      IF @STRRES=''
        SET @STRRES=@STRNOME
      ELSE
        SET @STRRES=@STRRES + ';' + @STRNOME
      FETCH NEXT FROM CURCOLONNE INTO @STRNOME
   END
  SELECT @STRCAMPICHIAVE = @STRRES
  CLOSE CURCOLONNE
  DEALLOCATE CURCOLONNE
END
