/*  DELETE TRIGGER "TD_OPERAZIONI" FOR TABLE "OPERAZIONI"  */
CREATE TRIGGER TD_OPERAZIONI ON OPERAZIONI FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    /*  DELETE ALL CHILDREN IN "LOGOPERAZIONI"  */
    DELETE LOGOPERAZIONI
    FROM   LOGOPERAZIONI T2, DELETED T1
    WHERE  T2.IDOP = T1.IDOP

    /*  DELETE ALL CHILDREN IN "SCADENZE"  */
    DELETE SCADOPERAZIONI
    FROM   SCADOPERAZIONI T2, DELETED T1
    WHERE  T2.IDOP = T1.IDOP

    /*  DELETE ALL CHILDREN IN "PARAMETRI"  */
    DELETE PAROPERAZIONI
    FROM   PAROPERAZIONI T2, DELETED T1
    WHERE  T2.IDOP = T1.IDOP

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR @ERRNO @ERRMSG
    ROLLBACK  TRANSACTION
END
GO

