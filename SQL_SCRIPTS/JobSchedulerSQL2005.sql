GO
CREATE PROCEDURE NUOVOPROGRESSIVO(@PTABELLA VARCHAR(80), @PAGGIORNA INTEGER, @PPROG DECIMAL(10) OUTPUT) AS
BEGIN
  DECLARE @PP DECIMAL(10)
  SELECT @PP=-1
  IF @PAGGIORNA=0
    BEGIN
    DECLARE CUR_ICONT CURSOR FOR SELECT PROGR FROM TABPROGRESSIVI WHERE NOMETABELLA=@PTABELLA
      OPEN CUR_ICONT
      FETCH CUR_ICONT INTO @PP
      IF @@FETCH_STATUS<>0     SELECT @PP = 0
      SELECT @PPROG = @PP+1
      CLOSE CUR_ICONT
      DEALLOCATE CUR_ICONT
    END
  ELSE
    BEGIN
      DECLARE CUR_ICONT CURSOR FOR SELECT PROGR FROM TABPROGRESSIVI WHERE NOMETABELLA=@PTABELLA FOR UPDATE
      OPEN CUR_ICONT
      FETCH CUR_ICONT INTO @PP
      IF @@FETCH_STATUS<>0
        BEGIN
           CLOSE CUR_ICONT
           INSERT INTO TABPROGRESSIVI(NOMETABELLA) VALUES(@PTABELLA)
           OPEN CUR_ICONT
           FETCH CUR_ICONT INTO @PP
        END
      SELECT @PP=@PP+1
      UPDATE TABPROGRESSIVI SET  PROGR=@PP WHERE CURRENT OF CUR_ICONT
      SELECT @PPROG=@PP
      CLOSE CUR_ICONT
      DEALLOCATE CUR_ICONT
    END
END
GO
